
import matplotlib.pyplot as plt
import numpy as np 

class Pwm():
    '''Generate senoidal reference for sPWM '''
    def __init__(self):
        self.top=100/2
        self._vecSin =np.zeros(49,dtype=int)
    
    def gera_pwm(self):
        for i in range(0,49):
            self._vecSin[i]=int(np.sin(2*np.pi*i/self.top)*self.top)+self.top
            i=i+1
        return self._vecSin
        


